fun main() {
//    Addition
    println(5 + 2)
    println("Harold" + "T")

//    Subtraction
    println(2 - 10)

//    Multiplication
    println(5 * 2)

//    Division
    println(5/2f)

//    Modulus
    println(5 % 2)

//    Increment
    var cats = 5
    cats++
    println(cats)

//    Decrement
    cats--
    println(cats)

//    Augmented assignment
    var bikes = 5
    bikes += 10
    println(bikes)

    var cars = 2
    cars -= 1
    println(cars)

    var motorBikes = 3
    motorBikes *= 2
    println(motorBikes)

    var boats = 20
    boats /= 2
    println(boats)

    var skates = 5
    skates %=2
    println(skates)
}